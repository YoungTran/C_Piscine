int	ft_iterative_factorial(int nb)
{
	int i;
	int j;

	if (nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	i = 1;
	j = 1;
	while (i <= nb)
	{
		j *= i;
		i++;
	}
	return (j);
}
