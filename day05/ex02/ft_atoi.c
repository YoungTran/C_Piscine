int	ft_atoi(char *str)
{
	int res;
	int sign;
	int i;

	res = 0;
	sign = 1;
	i = 0;
	while (str[i] && (str[i] > 57 || str[i] < 48))
	{
		if (str[i] == '-' && (str[i + 1] >= 48 || str[i + 1] <= 57))
			sign = -1;
		i++;
	}
	while (str[i] && ((str[i] >= 48 && str[i] <= 57)))
	{
		res *= 10;
		res = res + (str[i] - 48);
		i++;
	}
	return (res * sign);
}
